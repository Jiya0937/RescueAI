// Fallback for other regional languages
const getTranslation = (lang, key) => {
  const langObj = translations[lang] || translations['en'];
  return langObj[key] || translations['en'][key] || "";
};

window.RescueTranslations = {
  data: translations,
  get: getTranslation,
  apply: function(lang) {
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      const val = getTranslation(lang, key);
      if (!val) return;
      
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.setAttribute('placeholder', val);
      } else {
        el.innerHTML = val;
      }
    });

    // Update title tags
    const titleVal = getTranslation(lang, 'title');
    if (titleVal) document.title = titleVal;
    
    // Save to local storage
    localStorage.setItem('rescue_lang', lang);
  }
};
